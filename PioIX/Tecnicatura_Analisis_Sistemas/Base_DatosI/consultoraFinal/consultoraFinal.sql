CREATE DATABASE CONSULTORA COLLATE = "utf8_spanish_ci";

CREATE TABLE USUARIOS (ID_USUARIO INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, NOMBRE VARCHAR(12), APELLIDOS VARCHAR(25), EMAIL VARCHAR(140), FECHA_NACIMIENTO DATETIME);

CREATE TABLE OCUPACION (ID_OCUPACION INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, NOMBRE VARCHAR(100), FK_USUARIO INTEGER);

CREATE TABLE USUARIO_OCUPACION (ID_UO INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, FK_USUARIO INTEGER, FK_OCUPACION INTEGER);

CREATE TABLE INFORMES (ID_INFORME INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, TITULO VARCHAR(120), SUBTITULO VARCHAR(140), MINUTA VARCHAR(100), FECHA_PUBLICACION DATETIME, FK_USUARIO INTEGER, FK_TEMA INTEGER);

CREATE TABLE TEMAS (ID_TEMA INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, NOMBRE VARCHAR(120));

CREATE TABLE COMENTARIOS (ID_COMENTARIO INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, COMENTARIO VARCHAR(200), FK_INFORME INTEGER, FK_USUARIO INTEGER);

CREATE TABLE ARCHIVOS (ID_ARCHIVO INTEGER PRIMARY KEY NOT NULL AUTO_INCREMENT, ARCHIVOS VARCHAR (250), FK_INFORME INTEGER);



